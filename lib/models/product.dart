class Product {
  final String name;
  final String producer;
  final String price;
  final String unit;
  final String emoji;
  final String tag;
  final String category;

  const Product({
    required this.name,
    required this.producer,
    required this.price,
    required this.unit,
    required this.emoji,
    required this.tag,
    required this.category,
  });
}